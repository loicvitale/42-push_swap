#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	nbr_char;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write (fd, "-", 1);
			n *= -1;
		}
		if (n / 10)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
		else
		{
			nbr_char = n + '0';
			write (fd, &nbr_char, 1);
		}
	}
}
